    public static void search(String[] postIds) throws Exception {
        Map map = new HashMap();
        if (postIds != null) {
            for (String postId : postIds) {
                String data = getData(postId);
                map.put(postId, JSON.parseObject(data));
            }
        }

        renderJSON(map);
    }

    private static String getData(String postId) throws Exception {
        String expressType = getExpressType(postId);
        String detail = getDetail(expressType, postId);

        return detail;
    }

    private static String getExpressType(String postId) throws Exception {
        String data = "";
        for (int i = 0; i < 3; i++) {
            try {
                data = sendGet("http://www.kuaidi100.com/autonumber/autoComNum?resultv2=1&text=" + postId, null);
                JSONObject jsonObject = JSON.parseObject(data);
                JSONArray auto = jsonObject.getJSONArray("auto");
                String comCode = auto.getJSONObject(0).getString("comCode");

                return comCode;
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        return null;
    }

    private static String getDetail(String type, String postId) throws IOException {
        for (int i = 0; i < 3; i++) {
            try {
                String detail = sendGet("http://www.kuaidi100.com/query?type=" + type + "&postid=" + postId + "&temp=0.20209314532872624", createHeaders());
                return detail;
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        return null;
    }

    private static String sendGet(String url, Header[] headers) throws Exception {
        CloseableHttpClient client = HttpClientBuilder.create().build();

        HttpGet httpGet = new HttpGet(url);

        if (headers != null) httpGet.setHeaders(headers);

        StringBuilder stringBuilder = new StringBuilder();
        CloseableHttpResponse execute = null;
        try {
            execute = client.execute(httpGet);
            HttpEntity entity = execute.getEntity();
            InputStream content = entity.getContent();
            BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(content, "UTF-8"));
            String line;
            while ((line = bufferedReader.readLine()) != null) {
                stringBuilder.append(line).append("\r\n");
            }
        } catch (Exception e) {
            throw e;
        } finally {
            try {
                execute.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        return stringBuilder.toString();
    }

